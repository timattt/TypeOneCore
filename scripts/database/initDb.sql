create table article(
    articleId int primary key generated BY DEFAULT AS IDENTITY,
    title varchar(100),
    text varchar,
    currentLikes int check ( currentLikes > 0 ),
    commentsCount int check ( commentsCount > 0 )
);
create table comment(
    commentId int primary key generated by default as IDENTITY,
    author varchar,
    text varchar,
    articleId int references article(articleId) on delete CASCADE
);